package klik.actor;


// a container for aborting background tasks
//**********************************************************
public class Aborter
//**********************************************************
{
    private volatile boolean abort = false;

    //**********************************************************
    public void abort()
    //**********************************************************
    {
        abort = true;
    }
    //**********************************************************
    public boolean should_abort()
    //**********************************************************
    {
        return abort;
    }
}
